package day11_stringManipulations;

public class C09_RePlaceMetod {
    public static void main(String[] args) {

        String cumle = "Bugün havalar ne kadar da güzel.";

        System.out.println(cumle);                                   // Bugün havalar ne kadar da güzel

        System.out.println(cumle.replace('a', 'i')); // Bugün hivilir ne kidir di güzel.

        System.out.println(cumle.replace('ü', 'a'));    // Bugan havalar ne kadar da gazel



    }
}
